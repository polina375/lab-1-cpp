#pragma once
// Структура для хранения двумерного вектора (координаты x и y)
struct Vec2 {
    float x, y;
};

class Linear {
public:
    // Метод для линейного преобразования 2D-вектора с помощью матрицы 2x2
    //   matrix - двумерный массив 2x2, задающий преобразование
    //   x, y   - координаты исходной точки
    // Возвращает:
    //   Vec2 - новая точка после линейного преобразования
    static Vec2 transform(float matrix[2][2], float x, float y) {
        return { matrix[0][0] * x + matrix[0][1] * y,
                 matrix[1][0] * x + matrix[1][1] * y };
    }
        // Метод вычисляет значение прямой y = k * x + b для заданного x
        // Возвращает:
        //   абсолютное значение y (можно использовать для расчёта ошибки)
    static float line(float x, float k, float b) {
        float y = k * x + b;
        return y < 0 ? -y : y; // возвращаем абсолютное значение
    }
        //Метод для вычисления абсолютной разницы между двумя числами
        // Аргументы:
        //   a, b - значения для сравнения
        // Возвращает:
        //   абсолютное значение разницы |a - b|
    static float absDiff(float a, float b) {
        float d = a - b;
        return d < 0 ? -d : d;// если разница отрицательная, делаем её положительной
    }
};
